    public Carro execute(Carro c) {
        this.carroTemp = c;
        this.alterar = true;
        campoPlaca.setText(c.getPlaca());
        campoMarca.setText(c.getMarca());
        campoModelo.setText(c.getModelo());
        campoAno.setText(c.getAno().toString());
        campoValor.setText(c.getValorDiariaLocacao().toString());
        this.setVisible(true);
        return this.carroTemp;
    }

