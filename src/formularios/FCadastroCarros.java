    private void botaoGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoGravarActionPerformed
        try {
            Carro carro = null;
            if (this.alterar) {
                carro = this.carroTemp;
            } else {
                carro = new Carro();
            }
            carro.setPlaca(campoPlaca.getText());
            carro.setMarca(campoMarca.getText());
            carro.setModelo(campoModelo.getText());
            carro.setAno(Integer.parseInt(campoAno.getText()));
            carro.setValorDiariaLocacao(Double.parseDouble(campoValor.getText()));
            this.carroTemp = carro;
            this.setVisible(false);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    "N√£o foi poss√≠vel gravar o carro. \n\n" + ex.getMessage(),
                    "Erro",
                    JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_botaoGravarActionPerformed

